
public class De1a10 {
    //Escreva um programa que imprime os números de 1 a 10 usando 
    //um laço while.

    public static void main(String[] args) {
        System.out.println("Print de 1 a 10");

        for (int contador = 1; contador <= 10; contador++) {
            System.out.println(contador);

        }
    }

}
